package topic1;

public class Pojo {
    String name;
    String job;
    String level[];


    public String getJob() {
        return job;
    }

    public String getName(){
        return getName();
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setJob(String job) {
        this.job = job;
    }

    public String[] getLevel() {
        return level;
    }

    public void setLevel(String[] level) {
        this.level = level;
    }
}
